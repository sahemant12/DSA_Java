package com.Hemantsah._1.Basic;

public class TypeCasting {
    public static void main(String[] args) {
        int num = 258;
        int num2 = 200;
        int num3 = 129;

        byte b = (byte)num; //Explicit type casting
        byte c = (byte)num2;
        byte d = (byte)num3;
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
